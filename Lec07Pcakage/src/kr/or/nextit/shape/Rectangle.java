package kr.or.nextit.shape;

// 원래 풀 네임 -> kr.or.nextit.shape.Rectangle

public class Rectangle {

	public int width;
	public int height;
	
	public Rectangle(int width, int height){
		this.width = width;
		this.height = height;
	}
	
	public int getArea() {
		return width * height;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
}
